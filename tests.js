/*
Live Sampling POC requirements

General
-------
- it should listen to a sound source, record samples and reproduce them through the output channel
- it should have multiple modules ready to record and playback samples of the input signal (sound source)

Samples
--------
- it should have three states [record on, record off, play]
- it should be controlled by a single assignable key 
- it should be able to delete a sample 

Input
------
- it should be able to accept an input signal (sound source)
- it should be able to save the recording to assignable buttons

Output
---------
- it should output the sound source and sample playback 
- it should allow you to choose to cutoff ouput of the sound source at any given time
*/